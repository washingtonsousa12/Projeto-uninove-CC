      <h2 class="timeline-title" data-aos="fade" data-aos-duration="800">Nossa História</h2>
      <img src="assets/images/Group.png" alt="Logo Viajante Livre" class="timeline-logo" />

      <div class="timeline-container">
        <div class="timeline-item left">
          <h3>CRISTO REDENTOR</h3>
          <p>No topo da cidade está instalado Cristo Redentor, os lados turísticos mais procurados do Brasil e do Rio de Janeiro. Localizado no Parque Nacional da Tijuca, a 710 metros acima do nível do mar, onde qualquer um pode apreciar uma das mais belas vistas da cidade. Ao longo de todos os 220 degraus que levam aos famosos pés da estátua, foi eleita uma das Sete Maravilhas do Mundo.</p>
        </div>
        <div class="timeline-item right">
          <h3>PÃO DE AÇÚCAR</h3>
          <p>Um dos mais belos pontos turísticos do Rio de Janeiro e um dos mais famosos do Brasil. Trabalhamos com a missão de transportar pessoas para um mundo de encantamento, lazer e entretenimento, gerando experiências felizes, inesquecíveis e que valorizem a cultura brasileira.</p>
        </div>
        <div class="timeline-item left">
          <h3>Pedra da Gávea </h3>
          <p>
            A trilha da Pedra da Gávea é uma das mais procuradas do Rio de Janeiro, pois possui uma localização privilegiada e uma vista espetacular do litoral e montanhas da cidade. Pedra da Gávea tem 844 metros de altura, a trilha tem uma extensão de 2.600 km, leva por volta de 3 horas para chegar ao cume, é considerada uma trilha pesada..</p>
        </div>
        <div class="timeline-item right">
          <h3>Escadaria Selarón</h3>
          <p>Escadaria Selarón

            A Escadaria Selarón é uma obra arquitetônica localizada entre os bairros de Santa Teresa e Lapa, no Rio de Janeiro, Brasil, decorada pelo artista chileno radicado no Brasil de longa data, Jorge Selarón, que declarou-a como uma "homenagem ao povo brasileiro". </p>
        </div>
        <div class="timeline-item left">
          <h3>Praia de Copacabana</h3>
          <p>
            A Praia de Copacabana é uma praia localizada no bairro de Copacabana, na Zona Sul da cidade do Rio de Janeiro, no Brasil. É considerada uma das praias mais famosas do mundo.</p>
        </div>
        <div class="timeline-item right">
          <h3>Bondinho do Pão de Açúcar</h3>
          <p>
            O Bondinho do Pão de Açúcar é um dos cartões-postais mais famosos do Rio de Janeiro, oferecendo uma vista panorâmica incrível da cidade e da Baía de Guanabara.
          </p>
        </div>
